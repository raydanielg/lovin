    <script>
        // Dynamic Network Particles
        (function() {
            const canvas = document.getElementById('network-canvas');
            if (!canvas) return;
            
            const ctx = canvas.getContext('2d');
            let width, height;
            let particles = [];
            let mouseX = 0, mouseY = 0;
            
            function resize() {
                width = canvas.width = window.innerWidth;
                height = canvas.height = window.innerHeight;
            }
            
            function createParticle() {
                return {
                    x: Math.random() * width,
                    y: Math.random() * height,
                    vx: (Math.random() - 0.5) * 0.5,
                    vy: (Math.random() - 0.5) * 0.5,
                    size: Math.random() * 2 + 1,
                    color: Math.random() > 0.5 ? '#eab308' : '#16a34a'
                };
            }
            
            function init() {
                resize();
                for (let i = 0; i < 50; i++) {
                    particles.push(createParticle());
                }
            }
            
            function drawParticles() {
                ctx.clearRect(0, 0, width, height);
                
                // Update and draw particles
                particles.forEach((p, i) => {
                    p.x += p.vx;
                    p.y += p.vy;
                    
                    // Wrap around
                    if (p.x < 0) p.x = width;
                    if (p.x > width) p.x = 0;
                    if (p.y < 0) p.y = height;
                    if (p.y > height) p.y = 0;
                    
                    // Draw particle
                    ctx.beginPath();
                    ctx.arc(p.x, p.y, p.size, 0, Math.PI * 2);
                    ctx.fillStyle = p.color;
                    ctx.fill();
                    
                    // Draw connections
                    particles.forEach((p2, j) => {
                        if (i === j) return;
                        const dx = p.x - p2.x;
                        const dy = p.y - p2.y;
                        const dist = Math.sqrt(dx * dx + dy * dy);
                        
                        if (dist < 100) {
                            ctx.beginPath();
                            ctx.moveTo(p.x, p.y);
                            ctx.lineTo(p2.x, p2.y);
                            ctx.strokeStyle = `rgba(234, 179, 8, ${1 - dist / 100})`;
                            ctx.lineWidth = 0.5;
                            ctx.stroke();
                        }
                    });
                    
                    // Mouse interaction
                    const dx = mouseX - p.x;
                    const dy = mouseY - p.y;
                    const dist = Math.sqrt(dx * dx + dy * dy);
                    if (dist < 150) {
                        ctx.beginPath();
                        ctx.moveTo(p.x, p.y);
                        ctx.lineTo(mouseX, mouseY);
                        ctx.strokeStyle = `rgba(22, 163, 74, ${1 - dist / 150})`;
                        ctx.lineWidth = 1;
                        ctx.stroke();
                    }
                });
                
                requestAnimationFrame(drawParticles);
            }
            
            // Track mouse
            document.addEventListener('mousemove', (e) => {
                mouseX = e.clientX;
                mouseY = e.clientY;
            });
            
            window.addEventListener('resize', resize);
            init();
            drawParticles();
        })();
        
        // Typing effect for code lines
        (function() {
            const codeLines = document.querySelectorAll('.animate-type');
            codeLines.forEach((line, index) => {
                setTimeout(() => {
                    line.style.opacity = '1';
                }, parseInt(line.style.animationDelay) * 1000);
            });
        })();
        
        // Random packet animation
        (function() {
            function createPacket() {
                const container = document.getElementById('tech-illustration');
                if (!container) return;
                
                const packet = document.createElement('div');
                packet.className = 'absolute w-2 h-2 rounded-full animate-packet';
                packet.style.cssText = `
                    background: ${Math.random() > 0.5 ? '#eab308' : '#16a34a'};
                    left: ${Math.random() * 100}%;
                    top: ${Math.random() * 100}%;
                    --end-x: ${(Math.random() - 0.5) * 300}px;
                    --end-y: ${(Math.random() - 0.5) * 300}px;
                    animation-duration: ${2 + Math.random() * 2}s;
                `;
                
                container.appendChild(packet);
                
                setTimeout(() => {
                    packet.remove();
                }, 4000);
            }
            
            setInterval(createPacket, 2000);
        })();
        
        // Network nodes pulse effect
        (function() {
            const nodes = document.querySelectorAll('.animate-float-node');
            nodes.forEach((node, index) => {
                node.addEventListener('mouseenter', () => {
                    node.classList.add('glow-green');
                });
                node.addEventListener('mouseleave', () => {
                    node.classList.remove('glow-green');
                });
            });
        })();
        
        // Text Carousel - Smooth cycling through items
        (function() {
            const items = document.querySelectorAll('.carousel-item');
            if (items.length === 0) return;
            
            let currentIndex = 0;
            const intervalTime = 3000; // Change every 3 seconds
            
            function showNextItem() {
                // Remove active class from current item
                items[currentIndex].classList.remove('active');
                items[currentIndex].classList.add('prev');
                
                // Move to next index
                currentIndex = (currentIndex + 1) % items.length;
                
                // Small delay for transition effect
                setTimeout(() => {
                    // Remove prev class from all items
                    items.forEach(item => item.classList.remove('prev'));
                    
                    // Add active class to new current item
                    items[currentIndex].classList.add('active');
                }, 100);
            }
            
            // Start the carousel
            setInterval(showNextItem, intervalTime);
        })();
    </script>
    
